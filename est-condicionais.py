#IF / ELSE

idade = 18

if idade >= 18:
   print ("Você é maior de idade.")
else:
   print ("Você é menor de idade.")

#IF / ELIF / ELSE
#Quando uma avaliacao for positiva, a condicao ja retorna e nao avalia as proximas.

nota = 85

if nota >= 90:
   print ("Excelente")

elif nota >= 80:
   print ("Muito bom")

elif nota >= 70:
   print ("Bom")

else:
   print ("Precisa melhorar")

